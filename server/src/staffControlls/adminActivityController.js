/**
 * GET /api/admin/activities
 * Query: ?academicYearId=&participationType=INDIVIDUAL|TEAM&category=SPORTS|CULTURAL|ACADEMIC|OTHER
 */
export async function getActivities(req, res) {
  try {
    const schoolId = schoolGuard(req);
    const { academicYearId, participationType, category } = req.query;

    const activities = await prisma.activity.findMany({
      where: {
        schoolId,
        isArchived: false,                                      // ← soft-delete filter
        ...(academicYearId    ? { academicYearId }    : {}),
        ...(participationType ? { participationType } : {}),
        ...(category          ? { category }          : {}),
      },
      include: {
        academicYear:    { select: { id: true, name: true, isActive: true } },
        activityClasses: {
          include: {
            classSection: { select: { id: true, grade: true, section: true, name: true } },
          },
        },
        _count: {
          select: {
            enrollments: { where: { status: "ACTIVE" } },       // ← only active members
            events:      { where: { isAutoGenerated: false } },  // ← only real events
          },
        },
      },
      orderBy: { createdAt: "desc" },
    });

    return ok(res, { data: activities });
  } catch (e) {
    console.error("[getActivities]", e);
    return err(res, e.message, 500);
  }
}

