/**
 * POST /api/admin/activities/events
 * Body: {
 *   name, description, activityId (optional),
 *   eventType, participationMode (required),
 *   status, eventDate, venue,
 *   maxTeamsPerClass, maxStudentsPerClass,
 *   academicYearId, classSectionIds[]
 * }
 */
export async function createEvent(req, res) {
  try {
    const schoolId    = schoolGuard(req);
    const createdById = req.user.id;

    const {
      name,
      description,
      activityId,
      eventType,
      participationMode,              // ← required
      status = "DRAFT",
      eventDate,
      venue,
      maxTeamsPerClass,
      maxStudentsPerClass,
      academicYearId,
      classSectionIds = [],           // ← for EventClass eligibility
    } = req.body;

    if (!name)              return err(res, "name is required");
    if (!eventType)         return err(res, "eventType is required");
    if (!participationMode) return err(res, "participationMode is required (INDIVIDUAL, TEAM, BOTH)");
    if (!academicYearId)    return err(res, "academicYearId is required");

    const validEventTypes         = ["COMPETITION", "CULTURAL", "PARTICIPATION", "CEREMONY"];
    const validParticipationModes = ["INDIVIDUAL", "TEAM", "BOTH"];
    const validStatuses           = ["DRAFT", "PUBLISHED", "COMPLETED", "CANCELLED"];

    if (!validEventTypes.includes(eventType))
      return err(res, `eventType must be one of ${validEventTypes.join(", ")}`);
    if (!validParticipationModes.includes(participationMode))
      return err(res, `participationMode must be one of ${validParticipationModes.join(", ")}`);
    if (!validStatuses.includes(status))
      return err(res, `status must be one of ${validStatuses.join(", ")}`);

    const ay = await prisma.academicYear.findFirst({ where: { id: academicYearId, schoolId } });
    if (!ay) return err(res, "Academic year not found");

    if (activityId) {
      const act = await prisma.activity.findFirst({ where: { id: activityId, schoolId, isArchived: false } });
      if (!act) return err(res, "Activity not found");
    }

    const event = await prisma.activityEvent.create({
      data: {
        name,
        description,
        activityId:         activityId || null,
        eventType,
        participationMode,
        status,
        eventDate:          eventDate ? new Date(eventDate) : null,
        venue:              venue || null,
        maxTeamsPerClass:    maxTeamsPerClass    ? parseInt(maxTeamsPerClass)    : null,
        maxStudentsPerClass: maxStudentsPerClass ? parseInt(maxStudentsPerClass) : null,
        schoolId,
        academicYearId,
        createdById,
        eligibleClasses: {
          create: classSectionIds.map((csId) => ({ classSectionId: csId })),
        },
      },
      include: {
        activity:        { select: { id: true, name: true, participationType: true } },
        academicYear:    { select: { id: true, name: true } },
        eligibleClasses: {
          include: {
            classSection: { select: { id: true, grade: true, section: true, name: true } },
          },
        },
      },
    });

    return ok(res, { data: event }, 201);
  } catch (e) {
    console.error("[createEvent]", e);
    return err(res, e.message, 500);
  }
}

