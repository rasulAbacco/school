/**
 * POST /api/admin/activities
 * Body: { name, description, category, participationType, academicYearId, classSectionIds[] }
 *
 * NOTE: maxTeamsPerClass and maxStudentsPerClass belong on ActivityEvent, NOT here.
 */
export async function createActivity(req, res) {
  try {
    const schoolId    = schoolGuard(req);
    const createdById = req.user.id;

    const {
      name,
      description,
      category,
      participationType,
      academicYearId,
      classSectionIds = [],
    } = req.body;

    if (!name)              return err(res, "name is required");
    if (!category)          return err(res, "category is required (SPORTS, CULTURAL, ACADEMIC, OTHER)");
    if (!participationType) return err(res, "participationType is required (INDIVIDUAL, TEAM)");
    if (!academicYearId)    return err(res, "academicYearId is required");

    const validCategories       = ["SPORTS", "CULTURAL", "ACADEMIC", "OTHER"];
    const validParticipation    = ["INDIVIDUAL", "TEAM"];
    if (!validCategories.includes(category))          return err(res, `category must be one of ${validCategories.join(", ")}`);
    if (!validParticipation.includes(participationType)) return err(res, `participationType must be one of ${validParticipation.join(", ")}`);

    const ay = await prisma.academicYear.findFirst({ where: { id: academicYearId, schoolId } });
    if (!ay) return err(res, "Academic year not found");

    const activity = await prisma.activity.create({
      data: {
        name,
        description,
        category,
        participationType,
        schoolId,
        academicYearId,
        createdById,
        activityClasses: {
          create: classSectionIds.map((csId) => ({ classSectionId: csId })),
        },
      },
      include: {
        academicYear:    { select: { id: true, name: true } },
        activityClasses: {
          include: {
            classSection: { select: { id: true, grade: true, section: true, name: true } },
          },
        },
      },
    });

    return ok(res, { data: activity }, 201);
  } catch (e) {
    console.error("[createActivity]", e);
    return err(res, e.message, 500);
  }
}

/**
 * PUT /api/admin/activities/:id
 */
export async function updateActivity(req, res) {
  try {
    const schoolId = schoolGuard(req);
    const existing = await prisma.activity.findFirst({ where: { id: req.params.id, schoolId, isArchived: false } });
    if (!existing) return err(res, "Activity not found", 404);

    const {
      name,
      description,
      category,
      participationType,
      classSectionIds,
    } = req.body;

    const activity = await prisma.$transaction(async (tx) => {
      if (Array.isArray(classSectionIds)) {
        await tx.activityClass.deleteMany({ where: { activityId: req.params.id } });
        await tx.activityClass.createMany({
          data: classSectionIds.map((csId) => ({
            activityId:     req.params.id,
            classSectionId: csId,
          })),
        });
      }

      return tx.activity.update({
        where: { id: req.params.id },
        data: {
          ...(name              !== undefined ? { name }              : {}),
          ...(description       !== undefined ? { description }       : {}),
          ...(category          !== undefined ? { category }          : {}),
          ...(participationType !== undefined ? { participationType } : {}),
        },
        include: {
          academicYear:    { select: { id: true, name: true } },
          activityClasses: {
            include: {
              classSection: { select: { id: true, grade: true, section: true, name: true } },
            },
          },
        },
      });
    });

    return ok(res, { data: activity });
  } catch (e) {
    console.error("[updateActivity]", e);
    return err(res, e.message, 500);
  }
}

/**
 * DELETE /api/admin/activities/:id  →  SOFT ARCHIVE
 * Sets isArchived = true. Records are never hard-deleted.
 */
export async function deleteActivity(req, res) {
  try {
    const schoolId = schoolGuard(req);
    const existing = await prisma.activity.findFirst({ where: { id: req.params.id, schoolId, isArchived: false } });
    if (!existing) return err(res, "Activity not found", 404);

    // Block if any active members still enrolled
    const activeEnrollments = await prisma.studentActivityEnrollment.count({
      where: { activityId: req.params.id, status: "ACTIVE" },
    });
    if (activeEnrollments > 0) {
      return err(res, `Cannot archive: ${activeEnrollments} active student(s) enrolled. Remove members first.`, 409);
    }

    // Soft-archive the activity and its auto-generated events in one transaction
    await prisma.$transaction([
      prisma.activityEvent.updateMany({
        where: { activityId: req.params.id, isAutoGenerated: true },
        data:  { isArchived: true },
      }),
      prisma.activity.update({
        where: { id: req.params.id },
        data:  { isArchived: true },
      }),
    ]);

    return ok(res, { message: "Activity archived successfully" });
  } catch (e) {
    console.error("[deleteActivity]", e);
    return err(res, e.message, 500);
  }
}

